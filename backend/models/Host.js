import mongoose from "mongoose";

const hostSchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: true,
        },
        phone: {
            type: String,
            required: true,
            unique: true,
        },
        email: {
            type: String,
            required: true,
            unique: true,
        },
        password: {
            type: String,
            required: true,
        },
        role: {
            type: String,
            enum: ["host", "admin"],
            default: "host",
        },
    },
    { timestamps: true }
);

export default mongoose.model("Host", hostSchema);
